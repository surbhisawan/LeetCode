class MyCalendarThree {
public:
    map<int,int>mp;
    MyCalendarThree() {
        
    }
    
    int book(int start, int end) {
        mp[start]++;
        mp[end]--;
        
        int val=0,res=0;
        for(auto i:mp){
            val+=i.second;
            res=max(res,val);
        }
        return res;
    }
};

